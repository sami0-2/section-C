words = ["Hello", "world", "Python"]
print(" ".join(words))   # Output: "Hello world Python"

# Joining with a comma
print(", ".join(words))  # Output: "Hello, world, Python"

# Joining characters in a string
chars = ['H', 'e', 'l', 'l', 'o']
print("".join(chars))    # Output: "Hello"

# Joining numbers (convert to strings first)
numbers = ['1', '2', '3']
print("-".join(numbers))  # Output: "1-2-3"
